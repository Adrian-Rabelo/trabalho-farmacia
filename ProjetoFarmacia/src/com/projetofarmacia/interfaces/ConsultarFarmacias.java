        public void Buscar(String nome) {
        try {
            Farmacia far = new Farmacia();
            far.setIdFarmacia(TelaLogin.idFar);
            ProdutoDAO dao = new ProdutoDAO();
            List<Produto> listaDeProdutos = dao.buscarTodosProdutos(nome);
            DefaultTableModel modelo = (DefaultTableModel) tabelaFarmacias.getModel();
            modelo.setNumRows(0);
            Funcionario f = new Funcionario();
            f.setIdFuncionario(TelaLogin.idFunc);
            for (Produto p: listaDeProdutos) {
                modelo.addRow(new Object[] {
                    p.getIdProduto(),
                    p.getNomeProduto(),
                    p.getPreco(),
                    p.getQuantidade(),
                    p.getTarja(),
                    p.getFarmacia().getNomeFarmacia(),
                });
            }
        } catch (Exception e) {
            System.out.println("erro" + e);
            e.printStackTrace();
        }
    }
    public void Listar() {
        try {
            ProdutoDAO dao = new ProdutoDAO();
            List<Produto> listaDeProdutos = dao.listarTodosProdutos();
            DefaultTableModel modelo = (DefaultTableModel) tabelaFarmacias.getModel();
            modelo.setNumRows(0);
            
            for (Produto p: listaDeProdutos) {
                modelo.addRow(new Object[] {
                    p.getIdProduto(),
                    p.getNomeProduto(),
                    p.getPreco(),
                    p.getQuantidade(),
                    p.getTarja(),
                    p.getFarmacia().getNomeFarmacia(),
                });
            }
        } catch (Exception e) {
            throw new RuntimeException(e);
        }
        
    }
